# Starting a line with the pound symbol creates a comment
# Aka these lines will be ignored

# There are 4 main types of variables
# int -> a whole number
# float -> a number with a decimal
# string -> a literal interpretation of characters, must be surrouneded by ""
# boolean -> either True or False

# Setting a variable
# A single = will set the variable on the left of the equal sign

string_var = "This is a string"
int_var = 0
float_var = 0.0
boolean_var = True

# Use print() to display any information to the terminal
# Use type() to find what the type of the variable is

print("Hello World")
# This prints Hello World to the terminal

print(type(string_var))
# <class 'str'>
print(type(int_var))
# <class 'int'>
print(type(float_var))
# <class 'float'>
print(type(boolean_var))
# <class 'bool'>